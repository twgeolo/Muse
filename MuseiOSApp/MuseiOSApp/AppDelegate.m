- (NSData *)sendHTTPRequestWithPostStr: (NSString *)postStr andURL: (NSString *)URLStr {
    NSString *post = postStr;
    NSData *postData = [post dataUsingEncoding:NSASCIIStringEncoding allowLossyConversion:YES];
    NSString *postLength = [NSString stringWithFormat:@"%d", [postData length]];
    NSMutableURLRequest *request = [[NSMutableURLRequest alloc] init];
    [request setURL:[NSURL URLWithString:URLStr]];
    [request setHTTPMethod:@"POST"];
    [request setValue:postLength forHTTPHeaderField:@"Content-Length"];
    [request setValue:@"application/x-www-form-urlencoded" forHTTPHeaderField:@"Content-Type"];
    [request setHTTPBody:postData];
    NSData *data = [NSURLConnection sendSynchronousRequest:request returningResponse:nil error:nil];
    return data;
}

- (NSData *)sendHTTPRequestWithURL: (NSString *)URLStr {
    return [NSURLConnection sendSynchronousRequest:[[NSMutableURLRequest alloc] initWithURL:[NSURL URLWithString:URLStr]] returningResponse:nil error:nil];
}

